# Basic Functions (Q21-Q25)

# Q21 - Welcome message
def welcome():
    print("Welcome to Python Programming")

welcome()

# Q22 - Add two numbers
def add(a, b):
    print(a + b)

add(10, 20)

# Q23 - Greeting function
def greet(name):
    print("Hello,", name)

greet("Rohit")

# Q24 - Square of number
def square(num):
    print(num ** 2)

square(5)

# Q25 - Even or Odd
def even_odd(num):
    if num % 2 == 0:
        print("Even")
    else:
        print("Odd")

even_odd(8)